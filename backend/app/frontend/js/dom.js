export function setText(element, value, className = "") {
  if (!element) {
    return;
  }
  element.textContent = value;
  element.className = className;
}

export function toggleHidden(element, shouldHide) {
  if (!element) {
    return;
  }
  element.classList.toggle("hidden", shouldHide);
}
